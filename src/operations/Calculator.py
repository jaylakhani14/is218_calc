class Calculator:
    result = 0

    def _init_(self):
        pass

    def addition(a, b):
        return a + b

    def subtraction(a, b):
        return a - b

    def multiply(a, b):
        return a * b

    def division(a, b):
        return a / b

    def square(a):
        return a ** 2

    def square_root(a):
        return a ** 0.5



